var isGenerating = false;
function rand(arr) { return Math.floor(Math.random() * arr.length); }
function generateNFT() {
  if (isGenerating) return;
  isGenerating = true;
  setTimeout(function() { isGenerating = false; }, 10000);
  var btn = document.getElementById('generateBtn');
  var loading = document.getElementById('genLoading');
  var placeholder = document.getElementById('genPlaceholder');
  btn.disabled = true;
  loading.classList.add('active');
  placeholder.style.display = 'none';
  var bgIdx = rand(TRAITS['Background']);
  var unIdx = rand(TRAITS['Uniform']);
  var hdIdx = rand(TRAITS['Head']);
  var rhIdx = rand(TRAITS['Right Hand Object']);
  var lhIdx = rand(TRAITS['Left Hand Object']);
  var layers = [TRAITS['Background'][bgIdx],TRAITS['Uniform'][unIdx],TRAITS['Head'][hdIdx],TRAITS['Right Hand Object'][rhIdx],TRAITS['Left Hand Object'][lhIdx]];
  var names = [TRAIT_NAMES['Background'][bgIdx],TRAIT_NAMES['Uniform'][unIdx],TRAIT_NAMES['Head'][hdIdx],TRAIT_NAMES['Right Hand Object'][rhIdx],TRAIT_NAMES['Left Hand Object'][lhIdx]];
  // Random track
  var trackIdx = rand(VAULT_TRACKS);
  var track = VAULT_TRACKS[trackIdx];
  // Load all images
  var images = [];
  var loaded = 0;
  layers.forEach(function(src, i) {
    var img = new Image();
    img.onload = function() { images[i] = img; loaded++; if (loaded === layers.length) { renderNFT(images, names, track); } };
    img.onerror = function() { images[i] = null; loaded++; if (loaded === layers.length) { renderNFT(images, names, track); } };
    img.src = src;
  });
}
function renderNFT(images, names, track) {
  var canvas = document.getElementById('nftCanvas');
  var ctx = canvas.getContext('2d');
  ctx.clearRect(0, 0, 800, 800);
  images.forEach(function(img) {
    if (img) { ctx.drawImage(img, 0, 0, 800, 800); }
  });
  // Update UI
  genTokenId++;
  document.getElementById('nftTokenId').textContent = '#KY-' + String(genTokenId).padStart(4, '0');
  document.getElementById('nftEdition').textContent = 'Kaiseki Yuzu Collection - Temporary Preview';
  var layers = ['Background','Uniform','Head','Right Hand Object','Left Hand Object'];
  layers.forEach(function(layer, i) { var card = document.getElementById('traitCard' + i); var val = document.getElementById('traitVal' + i); val.textContent = names[i]; card.classList.add('has-trait'); });
  document.getElementById('nftTrackName').textContent = track.name;
  // Play track
  if (genAudio) { genAudio.pause(); genAudio = null; }
  genAudio = new Audio(track.src);
  genAudio.volume = 0.7;
  genAudio.play().catch(function() {});
  // Hide loading
  var loading = document.getElementById('genLoading');
  loading.classList.remove('active');
  var btn = document.getElementById('generateBtn');
  btn.textContent = '- Generate Again';
  btn.disabled = false;
  isGenerating = false;
}
