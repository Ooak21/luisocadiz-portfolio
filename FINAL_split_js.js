var walletConnected = false;
function connectWallet() {
  var btn = document.getElementById('walletBtn');
  var lbl = document.getElementById('walletLabel');
  var dot = document.getElementById('walletDot');
  if (walletConnected) {
    walletConnected = false;
    btn.style.borderColor = '#222222';
    btn.style.color = '#c0c0bb';
    dot.style.background = '#333330';
    dot.style.boxShadow = 'none';
    lbl.textContent = 'Connect Wallet';
    if (document.getElementById('vaultWalletAddress')) {
      document.getElementById('vaultWalletAddress').textContent = 'Wallet Unlocked';
    }
    hideVault();
    return;
  }
  if (typeof window.ethereum === 'undefined') {
    alert('No wallet found. Install MetaMask to connect.');
    return;
  }
  lbl.textContent = 'Connecting...';
  window.ethereum.request({ method: 'eth_requestAccounts' })
    .then(function(accounts) {
      var addr = accounts[0];
      var short = addr.slice(0,6) + '...' + addr.slice(-4);
      walletConnected = true;
      btn.style.borderColor = '#c8f04d';
      btn.style.color = '#c8f04d';
      dot.style.background = '#c8f04d';
      dot.style.boxShadow = '0 0 6px #c8f04d';
      lbl.textContent = 'Connected';
      if (document.getElementById('vaultWalletAddress')) {
        document.getElementById('vaultWalletAddress').textContent = short;
      }
      showVault();
    })
    .catch(function(err) {
      lbl.textContent = 'Connect Wallet';
    });
}
