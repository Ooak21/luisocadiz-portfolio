var playerAudio = null;
var playerCurrentIdx = 0;
var playerPlaying = false;
function playerLoadTrack(idx) {
  if (playerAudio) { playerAudio.pause(); playerAudio = null; }
  playerCurrentIdx = idx;
  var track = VAULT_TRACKS[idx];
  playerAudio = new Audio(track.src);
  playerAudio.volume = parseFloat(document.getElementById('playerVolSlider').value);
  playerAudio.addEventListener('timeupdate', playerUpdateProgress);
  playerAudio.addEventListener('loadedmetadata', function() {
    document.getElementById('playerTimeDuration').textContent = playerFmtTime(playerAudio.duration);
  });
  playerAudio.addEventListener('ended', function() { playerNext(); });
  document.getElementById('playerTrackName').textContent = track.name;
  document.getElementById('playerTrackNum').textContent = String(idx+1).padStart(2,'0') + ' / 08';
  document.getElementById('playerProgressFill').style.width = '0%';
  document.getElementById('playerTimeElapsed').textContent = '0:00';
  document.getElementById('playerTimeDuration').textContent = '0:00';
  document.querySelectorAll('.playlist-item').forEach(function(el, i) {
    el.classList.toggle('active', i === idx);
    el.classList.remove('playing');
  });
  if (playerPlaying) {
    playerAudio.play().catch(function(){});
    document.getElementById('playerPlayBtn').innerHTML = '&#10074;&#10074;';
    document.getElementById('playerViz').classList.add('playing');
    var item = document.getElementById('pli-' + idx);
    if (item) item.classList.add('playing');
  }
}
function playerToggle() {
  if (!playerAudio) { playerLoadTrack(0); }
  if (playerPlaying) {
    playerAudio.pause();
    playerPlaying = false;
    document.getElementById('playerPlayBtn').innerHTML = '&#9654;';
    document.getElementById('playerViz').classList.remove('playing');
    var item = document.getElementById('pli-' + playerCurrentIdx);
    if (item) item.classList.remove('playing');
  } else {
    playerAudio.play().catch(function(){});
    playerPlaying = true;
    document.getElementById('playerPlayBtn').innerHTML = '&#10074;&#10074;';
    document.getElementById('playerViz').classList.add('playing');
    var item = document.getElementById('pli-' + playerCurrentIdx);
    if (item) { item.classList.add('active'); item.classList.add('playing'); }
  }
}
function playerNext() {
  var next = (playerCurrentIdx + 1) % VAULT_TRACKS.length;
  playerPlaying = true;
  playerLoadTrack(next);
}
function playerPrev() {
  if (playerAudio && playerAudio.currentTime > 3) { playerAudio.currentTime = 0; return; }
  var prev = (playerCurrentIdx - 1 + VAULT_TRACKS.length) % VAULT_TRACKS.length;
  playerPlaying = true;
  playerLoadTrack(prev);
}
function playerUpdateProgress() {
  if (!playerAudio || !playerAudio.duration) return;
  var pct = (playerAudio.currentTime / playerAudio.duration) * 100;
  document.getElementById('playerProgressFill').style.width = pct + '%';
  document.getElementById('playerTimeElapsed').textContent = playerFmtTime(playerAudio.currentTime);
}
function playerSeek(e) {
  if (!playerAudio || !playerAudio.duration) return;
  var wrap = e.currentTarget;
  var rect = wrap.getBoundingClientRect();
  playerAudio.currentTime = ((e.clientX - rect.left) / rect.width) * playerAudio.duration;
}
function playerSetVol(v) { if (playerAudio) playerAudio.volume = parseFloat(v); }
function playerFmtTime(s) {
  if (isNaN(s)) return '0:00';
  var m = Math.floor(s/60);
  var sec = Math.floor(s%60);
  return m + ':' + (sec<10?'0':'') + sec;
}
function showGenerator() {
  var gen = document.getElementById('nftGenerator');
  if (gen) gen.style.display = '';
}
function hideGenerator() {
  var gen = document.getElementById('nftGenerator');
  if (gen) gen.style.display = 'none';
  if (genAudio) { genAudio.pause(); genAudio = null; }
}
function showVault() {
  document.getElementById('vaultTeaser').style.display = 'none';
  showGenerator();
  document.getElementById('vaultPlayer').classList.add('unlocked');
  playerLoadTrack(0);
}
function hideVault() {
  document.getElementById('vaultTeaser').style.display = '';
  document.getElementById('vaultPlayer').classList.remove('unlocked');
  hideGenerator();
  if (playerAudio) { playerAudio.pause(); playerAudio = null; }
  playerPlaying = false;
}

